package ATM_entity;

public class ChooseItem {
	private String Name;
	private int id;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String toString() {
		return Name;
	}
	
		
}
